#include <iostream>
using namespace std;
int main()
{
int c;
c=5;
cout<<c<<endl; //Ekrana 5 Yazdýr
cout<<c++<<endl; //Ekrana 5 Yazdýr ve Sonra c’yi 1 Arttýr
cout<<c<<endl<<endl; //Ekrana 6 Yazdýr
c=5;
cout<<c<<endl; //Ekrana 5 Yazdýr
cout<<++c<<endl; //c’yi 1 Arttýr ve Ekrana 6 Yazdýr
cout<<c<<endl; //Ekrana 6 Yazdýr
return 0;
}
